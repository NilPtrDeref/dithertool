};

pub const Mods = packed struct(u32) {
    shift: bool = false,
    control: bool = false,
    alt: bool = false,
    super: bool = false,
    caps_lock: bool = false,
    num_lock: bool = false,
    // GLFW uses bits 0-5. We pad the remaining 26 bits to fill the u32.
    _reserved: u26 = 0,
};

pub const EventQueue = Deque(Event);

pub const Event = union(enum) {
    WindowPos: struct { xpos: u32, ypos: u32 },
    WindowSize: struct { width: u32, height: u32 },
    WindowClose: void,
    WindowRefresh: void,
    WindowFocus: struct { focused: bool },
    WindowIconify: struct { iconified: bool },
    WindowMaximize: struct { iconified: bool },
    FramebufferSize: struct { width: u32, height: u32 },
    WindowContentScale: struct { xscale: f32, yscale: f32 },
    MouseButton: struct { button: MouseButton, action: Action, mods: Mods },
    CursorPos: struct { xpos: f64, ypos: f64 },
    CursorEnter: struct { entered: bool },
    Scroll: struct { xoffset: f64, yoffset: f64 },
    Key: struct { key: Key, scancode: u32, action: Action, mods: Mods },
    Char: struct { codepoint: u32 },
    Charmods: struct { codepoint: u32, mods: Mods },
    Drop: struct { window: *Window, paths: ArrayList([]const u8) },

    pub fn deinit(e: Event) void {
        switch (e) {
            .Drop => |d| {
                for (d.paths.items) |path| {
                    d.window.gpa.free(path);
                }
                var paths = d.paths;
                paths.deinit(d.window.gpa);
            },
            else => {},
        }
    }
};
